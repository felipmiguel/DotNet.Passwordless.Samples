using Microsoft.EntityFrameworkCore;
using Sample.Repository;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

builder.Services.AddDbContextFactory<ChecklistContext>(options =>
{
    string connectionString = builder.Configuration.GetSection("AZURE_MYSQL_CONNECTIONSTRING").Value;
    ServerVersion version = ServerVersion.Parse("5.7");
    options.UseMySql(connectionString, version).UseAadAuthentication();
});

var app = builder.Build();
using (var scope = app.Services.CreateScope())
{
    var services = scope.ServiceProvider;

    SeedData.Initialize(services);
}

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
